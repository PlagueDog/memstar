function preloadDirectoryFiles(%directory,%fileExtension)
{
    getDirectory(%directory);
    while(strlen($directoryFile[%i++]))
    {
        String::explode($directoryFile[%i], ".", fileName);
        if(String::findSubStr($directoryFile[%i], %fileExtension) > 0)
        {
            preloadFile($directoryFile[%i]);
            if(String::findSubStr($directoryFile[%i], ".ted" @ %fileExtension) > 0)
            {
                newObject($directoryFile[%i], SimVolume, $directoryFile[%i]);
                addToSet(PreloadedVolumes,$directoryFile[%i]);
                while(%ii++ <= 32)
                {
                    preloadFile($fileName @ ".ted#" @ %ii @ ".dtb");
                }
            }
        }
        $fileName="\x00";
    }
    deleteVariables("directoryFile*");
}

